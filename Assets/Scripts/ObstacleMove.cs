using UnityEngine;

public class ObstacleMove : MonoBehaviour
{
    public float speed = 5f;

    void Update(){
        // time.delta.time -> macht frame-unabh√§ngig
        transform.position += Vector3.left * speed * Time.deltaTime;

        if(transform.position.x < -15f){
            Destroy(gameObject);
        }
    }
}
